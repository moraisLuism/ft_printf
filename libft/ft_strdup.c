#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dest;
	size_t	len;

	i = 0;
	len = ft_strlen(s1);
	dest = (char *)malloc(len + 1);
	if (dest == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
