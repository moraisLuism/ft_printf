#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *) s;
	while (n > i)
	{
		str[i] = (unsigned char) c;
		i++;
	}
	return (str);
}
